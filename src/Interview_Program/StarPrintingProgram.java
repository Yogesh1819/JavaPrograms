package Interview_Program;

public class StarPrintingProgram {

	public static void main(String[] args) 
	{
		
		/*.........Right Handed Triangle................*/
		
		int rows=5;
		
		for (int i=1;i<=rows;i++)
		{
			for (int j=1;j<=i;j++)
			{
				System.out.print(" * ");
			}
			System.out.println();
		}
	
		

	}


}
